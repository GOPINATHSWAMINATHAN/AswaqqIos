import UIKit

class MainViewController: UIViewController {

    @IBOutlet weak var tableView: UITableView!
    
    @IBOutlet weak var home: UIWebView!
    
    
    override func viewDidLoad() {
        super.viewDidLoad()
        let url=URL(string: "https://www.aswaqqnet.com/web/en/andindex.php")
        let url2=URL(string:"https://www.aswaqqnet.com/web/ar/andindex.php")
        let pre=NSLocale.preferredLanguages[0]
        if(pre == "ar-US")
        {
            let urlRequest=URLRequest(url: url2!)
            home.loadRequest(urlRequest)
        }
        else
        {
            let urlRequest=URLRequest(url: url!)
            home.loadRequest(urlRequest)
        }
        
        //self.tableView.registerCellNib(DataTableViewCell.self)
    }
    
    override func viewWillTransition(to size: CGSize, with coordinator: UIViewControllerTransitionCoordinator) {
        super.viewWillTransition(to: size, with: coordinator)
    }
    
    override func viewWillAppear(_ animated: Bool) {
        super.viewWillAppear(animated)
        self.setNavigationBarItem()
    }

    override func didReceiveMemoryWarning() {
        super.didReceiveMemoryWarning()
    }

}


extension MainViewController : UITableViewDelegate {
    func tableView(_ tableView: UITableView, heightForRowAt indexPath: IndexPath) -> CGFloat {
        return DataTableViewCell.height()
    }
    
    func tableView(_ tableView: UITableView, didSelectRowAt indexPath: IndexPath) {
        let storyboard = UIStoryboard(name: "SubContentsViewController", bundle: nil)
        let subContentsVC = storyboard.instantiateViewController(withIdentifier: "SubContentsViewController") as! SubContentsViewController
        self.navigationController?.pushViewController(subContentsVC, animated: true)
    }
}


